"""
make_pricing_figures.py
=======================
Produce all chapter figures from Excel files generated by compare_swapvol.py
and CSV files from test_greeks.py.

Supports comparing multiple checkpoints (e.g., baseline vs joint training).

Figures produced
----------------
  fig_mae_heatmap_{label}.pdf         — (expiry × tenor) MAE heatmap comparison
  fig_vol_timeseries_{label}.pdf      — model vs market vol over time
  fig_vol_scatter_{label}.pdf         — model vol vs market vol scatter
  fig_greeks_comparison_{label}.pdf   — Greeks comparison across models

Usage
-----
    # Compare two models
    python make_pricing_figures.py \\
        --files checkpoint_dim4_ep3500 checkpoint_baseline_ep3500 \\
        --labels "Joint Training" "Baseline"
    
    # Single model
    python make_pricing_figures.py --files checkpoint_dim4_ep3500 --labels "My Model"

Or call run(files, labels, split_date, out_dir) from another script.
"""

